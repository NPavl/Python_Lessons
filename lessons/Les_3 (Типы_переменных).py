num_1 = input ("Enter first num: ")
num_2 = input ("Enter second num: ")
res = num_1 + num_2
print ("result is" , res) # я ввел 2 и 12.2 и на вывод получил 212.2 (тд не операцию сложения арифметическую)
# а результат сложения строк. если мы к примеру напишем Hello World то получим Hello world , так как данная
# операция выводит строки , соединяет их. для того чтобы использовать ка числа :
num_1 = int (input ("Enter first num: "))
num_2 = int (input ("Enter second num: ")) # таким образом мы переводим данные пользователя в тип int (float или str)
res = num_1 + num_2
print ("result is" , res) # итого результат именно арифметической операции .

# мы можем записать вот так :

# Типы переменных в Питон

test = 12 # в отличии от языка С мы просто обьявили переменную челочисленную (без типа) и присвоили ей значение 12
print (test) # Питон сам понимает какой тип перменной мы используем
test = "String" # аналогично вывод текста в консоль , нет необходимости создавать переменную типа char
print (test)
